/**
 * Returns the partner-specific logo if one exists (from partner.json or
 * built-in known logos), otherwise null (caller should use the default logo).
 */
export function getPartnerLogo(): string[] | null {
  // 1. Explicit logo in partner profile (partner.json)
  if (Partner.profile.logo && Partner.profile.logo.length > 0) {
    return Partner.profile.logo
  }
  // 2. Built-in known logo for this partner id
  const known = knownLogos[Partner.id]
  if (known) return known
  // 3. No custom logo — use default
  return null
}
